package cdpsession1;
//non-static and parameterized
public class Parameterized {
	public void add(int a, int b) { //Parameterized 
		int c=a+b;
		System.out.println(c);
	}
	public void sub(int a, int b) { //Parameterized 
		int c=a-b;
		System.out.println(c);
	}
	public void mul(int a, int b) { //Parameterized 
		int c=a*b;
		System.out.println(c);
	}
	public void div(int a, int b) { //Parameterized 
		int c=a/b;
		System.out.println(c);
	}
	

}
